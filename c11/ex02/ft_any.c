int	ft_any(char **tab, int(*f)(char*))
{
	int	returner;
	int	i;

	returner = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) != 0)
			returner = 1;
		i++;
	}
	return (returner);
}
