#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	if (*str == '\0')
		return ;
	while (*str != '\0')
	{
		ft_putchar(*str);
		str++;
	}
}
