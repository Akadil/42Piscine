void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	container;

	i = 0;
	while (i < size)
	{
		j = i;
		while (j < size)
		{
			if (tab[j] < tab[i])
			{
				container = tab[i];
				tab[i] = tab[j];
				tab[j] = container;
			}
			j++;
		}
		i++;
	}
}
